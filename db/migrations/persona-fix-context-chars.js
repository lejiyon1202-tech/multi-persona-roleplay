async function migrate() {
  const conn = await pool.getConnection();
  try {
    await conn.beginTransaction();

    for (const u of UPDATES) {
      const [result] = await conn.query(
        'UPDATE scenario_characters SET persona_prompt = ? WHERE name = ?',
        [u.persona, u.name]
      );
      console.log(`[MIGRATE] ${u.name}: ${result.affectedRows}건 업데이트`);
      if (result.affectedRows === 0) {
        console.warn(`[MIGRATE] ⚠️ ${u.name} — DB에서 캐릭터를 찾지 못했습니다.`);
      }
    }

    // 검증: "배경 인물", "대화 대상이 아닙니다" 문구 잔존 확인
    const [check] = await conn.query(
      `SELECT name, SUBSTRING(persona_prompt, 1, 200) AS preview
       FROM scenario_characters
       WHERE persona_prompt LIKE '%배경 인물%'
          OR persona_prompt LIKE '%대화 대상이 아닙니다%'
          OR persona_prompt LIKE '%컨텍스트 제공용%'`
    );
    if (check.length > 0) {
      console.error('[MIGRATE] ❌ 잔존 문구 발견:', check.map(r => r.name));
      await conn.rollback();
      process.exit(1);
    }

    await conn.commit();
    console.log('[MIGRATE] ✅ 컨텍스트 캐릭터 persona_prompt 정정 완료 — 4개 캐릭터');
    console.log('[MIGRATE] ✅ "배경 인물·대화 대상 아님" 잔존 문구 0건 PASS');
  } catch (err) {
    await conn.rollback();
    console.error('[MIGRATE] ❌ 오류:', err.message);
    process.exit(1);
  } finally {
    conn.release();
    await pool.end();
  }
}

migrate();
