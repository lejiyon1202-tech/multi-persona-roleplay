// ── 실행 ─────────────────────────────────────────────────────────────────────

async function backfill() {
  let connection;
  try {
    console.log('[BACKFILL v2] RDS MySQL 연결 중...');
    connection = await mysql.createConnection(config);
    console.log('[BACKFILL v2] 연결 성공.');

    // 1. 시나리오 백필
    for (const s of SCENARIO_UPDATES) {
      const [r] = await connection.query(
        `UPDATE scenarios
         SET learner_brief = ?, learner_mission = ?, learner_competencies = ?
         WHERE id = ?`,
        [s.learner_brief, s.learner_mission, s.learner_competencies, s.id]
      );
      console.log(`[BACKFILL v2] scenarios id=${s.id} → ${r.affectedRows}행 갱신`);
    }

    // 2. Case 1 & 2 캐릭터 백필 (scenario_id IN (1,2))
    for (const c of CASE12_CHAR_UPDATES) {
      const [r] = await connection.query(
        `UPDATE scenario_characters
         SET learner_detail = ?, emoji = ?
         WHERE scenario_id IN (1, 2) AND card_number = ?`,
        [JSON.stringify(c.learnerDetail), c.emoji, c.cardNumber]
      );
      console.log(`[BACKFILL v2] Case1&2 card_number=${c.cardNumber} → ${r.affectedRows}행 갱신`);
    }

    // 3. Case 3 캐릭터 백필 (scenario_id=4)
    for (const c of CASE3_CHAR_UPDATES) {
      const [r] = await connection.query(
        `UPDATE scenario_characters
         SET learner_detail = ?, emoji = ?
         WHERE scenario_id = 4 AND card_number = ?`,
        [JSON.stringify(c.learnerDetail), c.emoji, c.cardNumber]
      );
      console.log(`[BACKFILL v2] Case3 card_number=${c.cardNumber} → ${r.affectedRows}행 갱신`);
    }

    // 4. 검증 — JSON 유효성 + 필드 존재
    const [scenarios] = await connection.query(
      'SELECT id, learner_role, LEFT(learner_brief, 40) AS brief_preview FROM scenarios WHERE id IN (1,2,4)'
    );
    console.log('[BACKFILL v2] 시나리오 검증:', scenarios);

    const [chars] = await connection.query(
      `SELECT sc.scenario_id, sc.card_number, sc.name, sc.emoji,
              JSON_VALID(sc.learner_detail) AS detail_valid,
              JSON_UNQUOTE(JSON_EXTRACT(sc.learner_detail, '$.background'))     IS NOT NULL AS has_background,
              JSON_UNQUOTE(JSON_EXTRACT(sc.learner_detail, '$.speech_style'))   IS NOT NULL AS has_speech,
              JSON_UNQUOTE(JSON_EXTRACT(sc.learner_detail, '$.relationships'))  IS NOT NULL AS has_rel,
              JSON_UNQUOTE(JSON_EXTRACT(sc.learner_detail, '$.emotional_states')) IS NOT NULL AS has_emo,
              JSON_LENGTH(JSON_EXTRACT(sc.learner_detail, '$.ai_hints.first_utterances')) AS hint_count
       FROM scenario_characters sc
       WHERE sc.scenario_id IN (1,2,4)
       ORDER BY sc.scenario_id, sc.card_number`
    );

    let fail = 0;
    for (const c of chars) {
      const ok = c.detail_valid && c.has_background && c.has_speech && c.has_rel && c.has_emo && c.hint_count >= 5;
      if (!ok) {
        console.error(`[BACKFILL v2] ❌ FAIL scenario=${c.scenario_id} card=${c.card_number} ${c.name} hint=${c.hint_count} speech=${c.has_speech} rel=${c.has_rel} emo=${c.has_emo}`);
        fail++;
      } else {
        console.log(`[BACKFILL v2] ✅ OK  scenario=${c.scenario_id} card=${c.card_number} ${c.name} emoji=${c.emoji} hints=${c.hint_count}`);
      }
    }

    if (fail > 0) {
      console.error(`[BACKFILL v2] ❌ ${fail}건 FAIL — 수정 후 재실행`);
      process.exit(1);
    }

    console.log(`[BACKFILL v2] ✅ Phase C v3 detail-backfill v2 완료 — 시나리오 3건, 캐릭터 ${chars.length}명 전원 PASS`);
  } catch (err) {
    console.error('[BACKFILL v2] ❌ 오류:', err.message);
    process.exit(1);
  } finally {
    if (connection) await connection.end();
  }
}

backfill();
